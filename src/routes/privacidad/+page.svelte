			<h1>Política de Privacidad</h1>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">1. Identidad del Responsable</h2>
				<p>
					De conformidad con la Ley Federal de Protección de Datos Personales en Posesión de los
					Particulares, VOZEO (en adelante, “la Agencia”), con operaciones en México y registrada
					bajo un RFC de persona física con actividad empresarial, es responsable del uso y
					protección de los datos personales que recaba a través del sitio web www.vozeo.space.
				</p>
			</section>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">2. Datos personales que se recaban</h2>
				<p>Podemos recopilar los siguientes datos personales:</p>
				<ul class="list-disc ps-4">
					<li>Nombre completo o razón social</li>
					<li>Correo electrónico</li>
					<li>Número telefónico</li>
					<li>Información de contacto comercial o de marca</li>
					<li>Datos de facturación (RFC, domicilio fiscal, país, etc.)</li>
				</ul>
				<p>
					VOZEO no solicita datos sensibles como creencias religiosas, afiliaciones políticas o
					información médica.
				</p>
			</section>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">3. Finalidades del tratamiento</h2>
				<p>La información recabada se utiliza para:</p>
				<ul class="list-disc ps-4">
					<li>
						Proveer los servicios contratados (campañas digitales, automatización de embudos,
						estrategia de contenido, etc.)
					</li>
					<li>Dar seguimiento a solicitudes, cotizaciones o consultas enviadas por el usuario</li>
					<li>Mantener comunicación comercial y soporte técnico</li>
					<li>Emitir facturación y comprobantes fiscales</li>
					<li>Analizar resultados internos y optimizar la calidad del servicio</li>
				</ul>
				<p>
					De manera adicional, y con consentimiento previo, podremos enviar comunicaciones
					informativas o comerciales relacionadas con nuestros servicios.
				</p>
			</section>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">4. Medidas de seguridad</h2>
				<p>
					VOZEO implementa medidas administrativas, técnicas y físicas para proteger los datos
					personales contra daño, pérdida, alteración o uso no autorizado. Los datos se almacenan en
					servidores seguros con cifrado SSL y acceso restringido.
				</p>
			</section>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">5. Transferencia de datos</h2>
				<p>No compartimos tus datos personales con terceros, salvo en los siguientes casos:</p>
				<ul class="list-disc ps-4">
					<li>
						Cumplimiento de obligaciones legales o fiscales ante el SAT o autoridades competentes.
					</li>
					<li>
						Uso de herramientas tecnológicas de terceros (CRM, plataformas de automatización o
						servicios de hosting), bajo estrictos acuerdos de confidencialidad.
					</li>
				</ul>
			</section>
			<section class="space-y-4">
				<h2 class="font-extrabold md:text-3xl">6. Derechos ARCO</h2>
				<p>
					Tienes derecho a Acceder, Rectificar, Cancelar u Oponerte (ARCO) al tratamiento de tus
					datos personales. Para ejercer estos derechos, envía una solicitud al correo:
					<a href="mailto:privacidad@vozeo.space" class="font-extrabold text-blue hover:underline"
						>privacidad@vozeo.space</a
					>. Tu solicitud debe incluir:
				</p>
				<ul class="list-disc ps-4">
					<li>Nombre completo</li>
					<li>Descripción clara del derecho que deseas ejercer</li>
					<li>Copia de identificación oficial</li>
				</ul>

				<p>Responderemos en un plazo máximo de 15 días hábiles.</p>
			</section>
